package io.opensphere.core.util.fx.tabpane;

/**
 * An enumeration over the set of available drag states for tab pane tabs.
 */
public enum OSDragState
{
    /** The state for not dragging. */
    NONE,

    /** The state for when dragging starts. */
    START,

    /** The state for when tabs are reordering. */
    REORDER;
}
