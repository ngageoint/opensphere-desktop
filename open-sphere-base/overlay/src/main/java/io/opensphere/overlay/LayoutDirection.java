package io.opensphere.overlay;

/** An enumeration over the set of layout directions available for controls. */
public enum LayoutDirection
{
    /** Direction used to place a new control above an existing control. */
    NORTH,

    /** Direction used to place a new control right of an existing control. */
    EAST,

    /** Direction used to place a new control below an existing control. */
    SOUTH,

    /** Direction used to place a new control left of an existing control. */
    WEST;
}
